
public class pattern5 {
    public static void main (String args[]){
        int C = 5;
        int R = 6;
        for (int i = 0 ; i<C ; i++){
            for(int j = 0; j<R; j++){
                System.out.print(" * ");
            }
            System.out.println();
        }
    }    
}

